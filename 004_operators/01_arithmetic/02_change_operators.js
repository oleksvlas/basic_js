let num1 = -10;
let str1 = "15";
let result;

// Унарний мінус (-)
result = -num1; // Операція зміни знаку на протилежний.
console.log("-num1 = " + result);

/* Унарний плюс (+)
   не змінює знака при застосуванні до числового типу (нічого не відбувається). */
result = +num1;
console.log("+num1 = " + result);

/* Унарний плюс (+)
   при застосуванні до строковому типу, перетворює його в числовий. */
result = +str1 + num1;
console.log("+str + num1  = " + result);