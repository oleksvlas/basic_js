let a = 9;
let b = 5;
let result;

// Додавання (+)
result = a + b;
console.log("Результ додавання a+b: " + result);

// Віднімання (-)
result = a - b;
console.log("Результ віднімання a-b: " + result);

// Множення (*)
result = a * b;
console.log("Результ множення a*b: " + result);

// Ділення (/)
result = a / b;
console.log("Результ ділення a/b: " + result);

// Залишок від ділення (%)
result = a / b;
console.log("Результ залишку від ділення a/b: " + result);
console.log( 5 % 2 ); // 1, залишок від ділення 5 на 2
